public class DataDanVariabel {
     public static void main(String[] args) {
 // Tipe data primitif
 int umur = 30;
 double beratBadan = 65.5;
 char golonganDarah = 'O';
 boolean sudahMenikah = false;
 // Tipe data referensi
 String nama = "Budi Hartono";
 // Mencetak nilai variabel
 System.out.println("Nama: " + nama);
 System.out.println("Umur: " + umur + " tahun");
 System.out.println("Berat: " + beratBadan + " kg");
 System.out.println("Golongan Darah: " + golonganDarah);
 System.out.println("Status Menikah: " + sudahMenikah);
 // Mengubah nilai variabel (penugasan)
 umur = 31;
 System.out.println("Tahun depan, umur Budi: " + umur);
 }
}

    
